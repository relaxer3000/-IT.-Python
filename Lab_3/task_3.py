def count_letters(s):
    a = list(s.lower())
    z = {}
    d = 0
    for i in a:
        if i.isalpha():
            z[i] = z.get(i, d) + 1
    return z

# TODO Напишите функцию calculate_frequency
def calculate_frequency(s):
    a = 0
    for i in s:
        a += s.get(i)
    for j in s:
        s[j] = round(s[j] / a, 2)
    return s

